/**
 * Cari produk berdasarkan ID.
 * @param {number} id
 * @returns {object|undefined}
 */
export function getProductById(id) {
  return products.find((product) => product.id === id);
}

/**
 * Ambil semua produk dalam satu kategori.
 * @param {string} category — 'limbah-padat' | 'limbah-cair' | 'produk-turunan'
 * @returns {object[]}
 */
export function getProductsByCategory(category) {
  return products.filter((product) => product.category === category);
}

/**
 * Ambil produk-produk unggulan (isFeatured === true).
 * @returns {object[]}
 */
export function getFeaturedProducts() {
  return products.filter((product) => product.isFeatured);
}

/**
 * Cari produk berdasarkan kata kunci (nama, deskripsi, atau nama tanaman).
 * Pencarian bersifat case-insensitive.
 * @param {string} query
 * @returns {object[]}
 */
export function searchProducts(query) {
  const q = query.toLowerCase().trim();
  if (!q) return products;

  return products.filter((product) => {
    const haystack = [
      product.name,
      product.description,
      product.plant || '',
      product.categoryLabel,
      ...(product.solutions || []),
    ]
      .join(' ')
      .toLowerCase();

    return haystack.includes(q);
  });
}
